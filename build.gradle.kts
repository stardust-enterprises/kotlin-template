import java.net.URL
import java.time.OffsetDateTime
import java.time.format.DateTimeFormatter

/**
 * The JVM version we're currently targeting, or compiling to.
 */
val targetVersion = JavaVersion.VERSION_1_8

/**
 * The JVM version we're writing the project in.
 * Usually the same as the [targetVersion].
 */
val sourceVersion = JavaVersion.VERSION_1_8

/**
 * Whether we should generate an `api` source set.
 */
val generateApiSourceSet = true

// Project plugins, set up from the `buildSrc/.../plugins.kt` file
plugins {
    api.plugin.PLUGINS.forEach {
        id(it.id)
            .also { dep ->
                if (it.version != api.plugin.CORE_VERSION) {
                    dep.version(it.version)
                }
            }
            .apply(it.defaultApply)
    }
}

